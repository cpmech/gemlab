/// Calculates the extrapolation matrix (integration points to nodes)
///
/// ```text
/// u_nodal =   E    u_points
///  (nv)    (nv,np)   (np)
/// ```
///
/// Where `nv` is the number of vertices (nodes) and `np` is the number of integration points (`n_integ_point`).
///
/// # Input
///
/// * `pad` -- The scratchpad
/// * `integ_points` -- Integration points' constants (n_integ_point)
///
/// # Output
///
/// * `E` -- The (nnode,n_integ_point) extrapolation matrix; aka "inverse" of the
///    interpolation matrix `P` calculated by [get_interp_matrix()]. See note below
///    regarding the inversion problem.
///
/// This function returns the "inverse" of the interpolation matrix `P`; however, the
/// inverse is only possible if `n_integ_point == nnode`. If there are more interpolation
/// points than nodes (`n_integ_point > node`), the problem is over-determined, and we use
/// the pseudo-inverse instead `P⁺`. If there are fewer interpolation points than nodes
/// (`n_integ_point < nnode`), the problem is under-determined and may even yield
/// spurious results. To avoid such results and minimize the errors, we adopt the method
/// proposed by Durand and Farias in Reference #1. In this case, a correction matrix `A` is
/// applied to the pseudo-inverse, and a translation `ξ B` is applied to the result.
/// An exception exists: with a single integration point, the pseudo-inverse is returned
/// without any correction. In summary:
///
/// ```text
///     ⎧ P⁻¹         if np = nv (exact)
/// E = ⎨ P⁺          if np > nv (over-determined)
///     │ P⁺          if np = 1 < nv (exception case)
///     ⎩ P⁺ A + ξ B  if np < nv (under-determined)
/// ```
///
/// where `ξ` is a matrix with the reference (natural) coordinates of all nodes (vertices),
/// augmented by a column of ones; see Eq. (32)-(34) of Ref #1. Let us define `hat_ξ`
/// as a matrix with the reference coordinates all integration points (sampling points),
/// augmented by a column of ones; see Eq. (32)-(34). Then we have that:
///
/// ```text
/// B = pseudo_inverse(hat_ξ)
/// A = I - hat_ξ B
/// ```
///
/// where `I` is an identity matrix with n_integ_points rows and columns.
///
/// With `nd` being the number of geometry dimensions plus 1, we have that:
///
/// ```text
/// P : np × nv     hat_ξ: np × nd
/// P⁺: nv × np     B    : nd × np
/// ξ : nv × nd     I    : np × np,   
/// ```
///
/// Thus
///
/// ```text
///    A   ←   I   - hat_ξ  *   B
/// (np,np) (np,np) (np,nd)  (nd,np)
///
///    E   ←   P⁺  *   A   +   ξ   *   B
/// (nv,np) (nv,np) (np,np) (nv,nd) (nd,np)
/// ```
///
/// # Reference
///
/// 1. Durand R and Farias MM (2014) A local extrapolation method for finite elements,
///    Advances in Engineering Software, 67:1-9 <https://doi.org/10.1016/j.advengsoft.2013.07.002>
///
/// # Examples
///
/// ```
/// use gemlab::integ::Gauss;
/// use gemlab::recovery::{get_extrap_matrix, get_interp_matrix};
/// use gemlab::shapes::{GeoKind, Scratchpad};
/// use gemlab::StrError;
/// use russell_lab::{mat_vec_mul, vec_approx_eq, Vector};
///
/// fn main() -> Result<(), StrError> {
///     //  6 2
///     //  5 | `.    * indicates the
///     //  4 | * `.    location of ips
///     //  3 |     `.
///     //  2 |       `.
///     //  1 | *     * `.
///     //  0 0-----------1
///     //    0 1 2 3 4 5 6
///
///     let space_ndim = 2;
///     let mut pad = Scratchpad::new(space_ndim, GeoKind::Tri3)?;
///     pad.set_xx(0, 0, 0.0);
///     pad.set_xx(0, 1, 0.0);
///     pad.set_xx(1, 0, 6.0);
///     pad.set_xx(1, 1, 0.0);
///     pad.set_xx(2, 0, 0.0);
///     pad.set_xx(2, 1, 6.0);
///
///     // nodal values
///     let u_nodal = Vector::from(&[1.0, 2.0, 3.0]);
///
///     // interpolated values
///     let gauss = Gauss::new_sized(pad.kind.class(), 3)?;
///     let mut u_points = Vector::new(gauss.npoint());
///     let pp = get_interp_matrix(&mut pad, &gauss);
///     mat_vec_mul(&mut u_points, 1.0, &pp, &u_nodal)?;
///
///     // extrapolated values (recovered)
///     let nnode = pad.xxt.dims().1;
///     let mut u_nodal_rec = Vector::new(nnode);
///     let ee = get_extrap_matrix(&mut pad, &gauss)?;
///     mat_vec_mul(&mut u_nodal_rec, 1.0, &ee, &u_points)?;
///
///     // check
///     vec_approx_eq(&u_nodal_rec, &u_nodal, 1e-14);
///     Ok(())
/// }
/// ```
pub fn get_extrap_matrix(pad: &mut Scratchpad, gauss: &Gauss) -> Result<Matrix, StrError> {
    // constants
    let (nv, geo_ndim) = pad.deriv.dims();
    let np = gauss.npoint();

    // calculate interpolation matrix P
    let mut pp = get_interp_matrix(pad, gauss);

    // allocate output
    let mut ee = Matrix::new(nv, np);

    // handle cases
    if np == nv {
        // E ← P⁻¹
        mat_inverse(&mut ee, &pp)?;
    } else if np > nv {
        // E ← P⁺
        mat_pseudo_inverse(&mut ee, &mut pp)?;
    } else if np == 1 {
        // E ← P⁺
        mat_pseudo_inverse(&mut ee, &mut pp)?;
    } else {
        // P⁺ ← pseudo-inverse of interpolation matrix
        let mut ppi = Matrix::new(nv, np);
        mat_pseudo_inverse(&mut ppi, &mut pp)?;

        // Bᵀ ← transpose(pinv(hat_ξ))
        let bbt = get_tr_pinv_hxi(pad.kind.class(), np);

        //    A   ←   I   - hat_ξ *   B
        // (np,np) (np,np) (np,nd) (nd,np)
        let mut aa = Matrix::new(np, np);
        for i in 0..np {
            for j in 0..np {
                let mut sum = 0.0;
                for k in 0..geo_ndim {
                    sum += gauss.coords(i)[k] * bbt[j][k];
                }
                sum += 1.0 * bbt[j][geo_ndim]; // 1.0 = gauss.coords(i)[geo_ndim]
                let d_ij = if i == j { 1.0 } else { 0.0 };
                aa.set(i, j, d_ij - sum);
            }
        }

        //    E   ←   ξ   *   B
        // (nv,np) (nv,nd) (nd,np)
        for i in 0..nv {
            let rci = pad.kind.reference_coords(i);
            for j in 0..np {
                for k in 0..geo_ndim {
                    ee.add(i, j, rci[k] * bbt[j][k]);
                }
                ee.add(i, j, 1.0 * bbt[j][geo_ndim]); // 1.0 = rci[geo_ndim]
            }
        }

        //    E   +=   P⁺  *   A
        // (nv,np)  (nv,np) (np,np)
        for i in 0..nv {
            for j in 0..np {
                for k in 0..np {
                    ee.add(i, j, ppi.get(i, k) * aa.get(k, j));
                }
            }
        }
    }
    return Ok(ee);
}

/// Returns the transpose of the pseudo-inverse of the reference coordinates matrix
///
/// # Output
///
/// ```text
/// TR_PINV_HXI ← transpose(PINV_HXI) ← transpose(pinv(hat_ξ))
/// ```
///
/// where `PINV_HXI ← pinv(hat_ξ)` and `HXI ← hat_ξ` is a matrix with the reference
/// (natural) coordinates of the integration points, augmented by a column of ones.
/// Here, `pinv` means the pseudo-inverse. See Reference #1.
///
/// The reason for returning the transpose is due to the way static arrays are
/// handled in Rust. With the transpose, we can share a references to the static
/// data from any GeoClass. To do so, we also save the transpose matrix with
/// 4 columns, even if geo_ndim < 3.
///
/// Note that the columns of the resulting matrix are padded with zeros if geo_ndim < 3.
///
/// # Reference
///
/// 1. Durand R and Farias MM (2014) A local extrapolation method for finite elements,
///    Advances in Engineering Software, 67:1-9 <https://doi.org/10.1016/j.advengsoft.2013.07.002>
fn get_tr_pinv_hxi(class: GeoClass, n_integ_point: usize) -> &'static [[f64; 4]] {
    match class {
        // Lin
        GeoClass::Lin => match n_integ_point {
            1 => &TR_PINV_HXI_LIN_LEGENDRE_1,
            2 => &TR_PINV_HXI_LIN_LEGENDRE_2,
            3 => &TR_PINV_HXI_LIN_LEGENDRE_3,
            4 => &TR_PINV_HXI_LIN_LEGENDRE_4,
            5 => &TR_PINV_HXI_LIN_LEGENDRE_5,
            _ => unreachable!("requested number of integration points is not available for Lin class"),
        },
        // Tri
        GeoClass::Tri => match n_integ_point {
            1 => &TR_PINV_HXI_TRI_INTERNAL_1,
            3 => &TR_PINV_HXI_TRI_INTERNAL_3,
            4 => &TR_PINV_HXI_TRI_INTERNAL_4,
            6 => &TR_PINV_HXI_TRI_FELIPPA_6,
            7 => &TR_PINV_HXI_TRI_FELIPPA_7,
            12 => &TR_PINV_HXI_TRI_INTERNAL_12,
            16 => &TR_PINV_HXI_TRI_INTERNAL_16,
            _ => unreachable!("requested number of integration points is not available for Tri class"),
        },
        // Qua
        GeoClass::Qua => match n_integ_point {
            1 => &TR_PINV_HXI_QUA_LEGENDRE_1,
            4 => &TR_PINV_HXI_QUA_LEGENDRE_4,
            9 => &TR_PINV_HXI_QUA_LEGENDRE_9,
            16 => &TR_PINV_HXI_QUA_LEGENDRE_16,
            _ => unreachable!("requested number of integration points is not available for Qua class"),
        },
        // Tet
        GeoClass::Tet => match n_integ_point {
            1 => &TR_PINV_HXI_TET_INTERNAL_1,
            4 => &TR_PINV_HXI_TET_INTERNAL_4,
            5 => &TR_PINV_HXI_TET_INTERNAL_5,
            8 => &TR_PINV_HXI_TET_FELIPPA_8,
            14 => &TR_PINV_HXI_TET_FELIPPA_14,
            15 => &TR_PINV_HXI_TET_FELIPPA_15,
            24 => &TR_PINV_HXI_TET_FELIPPA_24,
            _ => unreachable!("requested number of integration points is not available for Tet class"),
        },
        // Hex
        GeoClass::Hex => match n_integ_point {
            6 => &TR_PINV_HXI_HEX_IRONS_6,
            8 => &TR_PINV_HXI_HEX_LEGENDRE_8,
            14 => &TR_PINV_HXI_HEX_IRONS_14,
            27 => &TR_PINV_HXI_HEX_LEGENDRE_27,
            64 => &TR_PINV_HXI_HEX_LEGENDRE_64,
            _ => unreachable!("requested number of integration points is not available for Hex class"),
        },
    }
}

